package com.example.firstandroidcode;

public class Fruit
{
    private String mName;
    private int mImageId;

    public Fruit(String mName, int mImageId)
    {
        this.mName = mName;
        this.mImageId = mImageId;
    }

    public String getmName()
    {
        return mName;
    }

    public int getmImageId()
    {
        return mImageId;
    }
}
